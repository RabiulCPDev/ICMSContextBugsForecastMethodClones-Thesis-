    public void saveSystemInformation (String repositoryPath, String language, int startRevision, int endRevision, String connectionstring)            
    {
        try
        {
            String systemName = "";
            systemName = repositoryPath;
            BufferedWriter br = new BufferedWriter (new FileWriter ("common_parameters.txt"));
            br.write ("subject system = "+repositoryPath+"\n");
            br.write ("subject system path = "+repositoryPath+"/repository"+"\n");
            br.write ("programming language = "+language+"\n");
            br.write ("revision count = "+endRevision+"\n");
            br.write ("database connection string = "+ connectionstring+"\n");
            br.write("separator =  : \n");
            br.write("clone type index = 1");
            br.close();

            File systemDirectory = new File(systemName);
            if (!systemDirectory.exists())
            {
                systemDirectory.mkdir();
            }

            File methodDirectory = new File (systemName+"/methods");
            if (!methodDirectory.exists())
            {
                methodDirectory.mkdir();
            }

            File cloneDirectory = new File (systemName+"/clones");
            if (!cloneDirectory.exists())
            {
                cloneDirectory.mkdir();
            }

            File changeDirectory = new File (systemName+"/changes");
            if (!changeDirectory.exists())
            {
                changeDirectory.mkdir();
            }
            
            File cmethodDirectory = new File (systemName+"/clonedmethods");
            if (!cmethodDirectory.exists())
            {
                cmethodDirectory.mkdir();
            }

            BufferedWriter br1 = new BufferedWriter (new FileWriter (systemName+"/maxglobalmethodid.txt"));
            br1.write("0");
            br1.close();
            
            
            /*ProjectCrossboundary p = new ProjectCrossboundary();
            p.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            p.setVisible(true);*/
            
            AccessDialogs ad = new AccessDialogs ();
            ad.setDefaultCloseOperation (JFrame.EXIT_ON_CLOSE);
            ad.setVisible(true);
        }
        catch (Exception e)
        {
            System.out.println ("error. method name = jButton1ActionPerformed");
        }        
    }
    
    
